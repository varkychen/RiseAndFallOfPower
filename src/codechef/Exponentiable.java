package codechef;

import java.math.BigInteger;

public class Exponentiable {
	public BigInteger npowern(BigInteger n, BigInteger e) { throw new UnsupportedOperationException("No default implementation"); }
	
	protected long npowern(long n, long e) { throw new UnsupportedOperationException("No default implementation"); }
	public long npowern(long n) {
		return npowern(n,n);
	}
	
	protected String[] npowern(int n, int e) { throw new UnsupportedOperationException("No default implementation"); }
	public String[] npowern(int n) {
		return npowern(n,n);
	}
}